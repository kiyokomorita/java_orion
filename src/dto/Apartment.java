package dto;

public class Apartment {
	//物件用の変数宣言
	private Integer apartmentCode;

	private String apartmentName;

	private String apartmentDescription;

	private Integer apartmentNumber;

	private Integer apartmentPrice;

	private String apartmentPet;

	private String apartmentLayout;

	private String apartmentImage;

	private String apartmentAddress1;

	private String apartmentAddress2;

	private String apartmentStatus;

	//ゲッター・セッター
	public Integer getApartmentCode() {
		return apartmentCode;
	}

	public void setApartmentCode(Integer apartmentCode) {
		this.apartmentCode = apartmentCode;
	}

	public String getApartmentName() {
		return apartmentName;
	}

	public void setApartmentName(String apartmentName) {
		this.apartmentName = apartmentName;
	}

	public String getApartmentDescription() {
		return apartmentDescription;
	}

	public void setApartmentDescription(String apartmentDescription) {
		this.apartmentDescription = apartmentDescription;
	}

	public Integer getApartmentNumber() {
		return apartmentNumber;
	}

	public void setApartmentNumber(Integer apartmentNumber) {
		this.apartmentNumber = apartmentNumber;
	}

	public Integer getApartmentPrice() {
		return apartmentPrice;
	}

	public void setApartmentPrice(Integer apartmentPrice) {
		this.apartmentPrice = apartmentPrice;
	}

	public String getApartmentPet() {
		return apartmentPet;
	}

	public void setApartmentPet(String apartmentPet) {
		this.apartmentPet = apartmentPet;
	}

	public String getApartmentLayout() {
		return apartmentLayout;
	}

	public void setApartmentLayout(String apartmentLayout) {
		this.apartmentLayout = apartmentLayout;
	}

	public String getApartmentImage() {
		return apartmentImage;
	}

	public void setApartmentImage(String apartmentImage) {
		this.apartmentImage = apartmentImage;
	}

	public String getApartmentAddress1() {
		return apartmentAddress1;
	}

	public void setApartmentAddress1(String apartmentAddress1) {
		this.apartmentAddress1 = apartmentAddress1;
	}

	public String getApartmentAddress2() {
		return apartmentAddress2;
	}

	public void setApartmentAddress2(String apartmentAddress2) {
		this.apartmentAddress2 = apartmentAddress2;
	}

	public String getApartmentStatus() {
		return apartmentStatus;
	}

	public void setApartmentStatus(String apartmentStatus) {
		this.apartmentStatus = apartmentStatus;
	}
}
